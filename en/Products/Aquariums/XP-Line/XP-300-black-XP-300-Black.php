		loader.show()			   // loader einblenden

		// url für Request
		// var url = '<?php echo $site_url;?>partials/produkt-detail/ajax-content/ajax.php/'
		var sData = "cl=srajax&fnc=getArticleContent&tpl=srarticlecontent.tpl&anid=" + iArtId + "";

