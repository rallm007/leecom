		} else {
			var sUrl = "../../../../index.php-lang=1&cl=srajax.php"/*tpa=<?php echo $site_url;?>index.php?lang=1&cl=srajax*/; 
		}

		// ajax request
		$.ajax({
			type: "POST",
			url: sUrl,
			data: sData,
			complete: function(result) {

					var data = result.responseText;
					// nach request
					contentBox.empty(); // alten Content löschen
					contentBox.append(data); // Box mit neuen Conetent befüllen
